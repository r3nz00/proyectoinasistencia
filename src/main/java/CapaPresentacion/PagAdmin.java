    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
int filaSeleccionada = tableInasistencias.getSelectedRow();
    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(this, "Por favor, seleccione una fila para eliminar.", "Atención", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Obtener los valores de la fila seleccionada
    String nombre = (String) tableInasistencias.getValueAt(filaSeleccionada, 0);
    String apellido = (String) tableInasistencias.getValueAt(filaSeleccionada, 1);
    String materia = (String) tableInasistencias.getValueAt(filaSeleccionada, 2);
    String grupo = (String) tableInasistencias.getValueAt(filaSeleccionada, 3);
    String fechaInicio = (String) tableInasistencias.getValueAt(filaSeleccionada, 4);
    String fechaFinal = (String) tableInasistencias.getValueAt(filaSeleccionada, 5);

    int opcion = JOptionPane.showConfirmDialog(this,
        "¿Seguro que desea eliminar la inasistencia de " + nombre + " " + apellido + "?",
        "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

    if (opcion == JOptionPane.YES_OPTION) {
        try {
            CapaPersistencia.PersistenciaInasistencias persistencia = new CapaPersistencia.PersistenciaInasistencias();
            boolean eliminada = persistencia.eliminarInasistencia(nombre, apellido, materia, grupo, fechaInicio, fechaFinal);

            if (eliminada) {
                JOptionPane.showMessageDialog(this, "Inasistencia eliminada correctamente.");
                cargarTablaInasistencias();
            } else {
                JOptionPane.showMessageDialog(this, "No se encontró la inasistencia a eliminar.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al eliminar la inasistencia: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnEliminarActionPerformed

