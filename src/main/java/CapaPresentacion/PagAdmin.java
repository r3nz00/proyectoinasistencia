    private void cargarTablaInasistencias() 
    
    {
    try {
        PersistenciaInasistencias persistencia = new PersistenciaInasistencias();
        List<Inasistencia> lista = persistencia.obtenerTodas();

        DefaultTableModel modelo = (DefaultTableModel) tableInasistencias.getModel();
        modelo.setRowCount(0); // Limpia la tabla

        for (Inasistencia i : lista) {
            Object[] fila = {
                i.getNombre(),
                i.getApellido(),
                i.getMateria(),
                i.getGrupo(),
                i.getFechaInicio(),
                i.getFechaFinal()
            };
            modelo.addRow(fila);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al cargar inasistencias: " + e.getMessage());
    }
}


